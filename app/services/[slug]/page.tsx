export function generateStaticParams() {
  return services.map((service) => ({
    slug: service.id
  }));
}

function getService(slug: string) {
  return services.find((service) => service.id === slug);
}

function getStoryExamples(config: ServiceStoryConfig) {
  return config.examples.slugs
    .map((exampleSlug) => projects.find((project) => project.slug === exampleSlug))
    .filter((project): project is Project => Boolean(project));
}

export async function generateMetadata({ params }: ServicePageProps) {
  const { slug } = await params;
  const service = getService(slug);

  if (!service) {
    return {};
  }

  return createPageMetadata({
    title: `${service.title} — ДесмосАвто`,
    description: `${service.description} Демо-версия за сутки без предоплаты для автомобильного бизнеса.`,
    path: `/services/${service.id}`
  });
}

